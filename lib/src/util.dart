// xtob converts hex characters x1 and x2 into a byte.
Tuple2<int, bool> xtob(int x1, int x2) {
  final b1 = xvalues[x1];
  final b2 = xvalues[x2];
  return Tuple2((b1 << 4) | b2, b1 != 255 && b2 != 255);
}
